<?php

namespace App\Mail\Beluga;

use Illuminate\Bus\Queueable;
use Illuminate\Contracts\Queue\ShouldQueue;
use Illuminate\Mail\Mailable;
use Illuminate\Queue\SerializesModels;

class CancellationMailSentToBeluga extends Mailable
{
    use Queueable, SerializesModels;

    private $data;
    /**
     * Create a new message instance.
     *
     * @return void
     */
    public function __construct($data)
    {
        $this->data = $data;
    }

    /**
     * Build the message.
     *
     * @return $this
     */
    public function build()
    {
        return $this->view('emails.beluga.cancellation-email-send-to-beluga', ['order' => $this->data])
            ->subject("Customer Service Email for Visit Id: $this->data->masterId");
    }
}
